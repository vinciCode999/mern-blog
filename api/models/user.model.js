import mongoose from "mongoose"

const UserSchema = new mongoose.Schema({
  username: {
    type: String,
    unique: true,
    required: true
  },
  email: {
    type: String,
    unique: true,
    required: true
  },
  password: {
    type: String,
    required: true
  }
}, {timeStamps: true})

const User = mongoose.model('user', UserSchema)
 
export default User